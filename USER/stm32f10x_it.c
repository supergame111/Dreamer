//这里i= 70大概为1ms
static void key_delay(u16 i)
{
	u16 count = 0;
	while(i--)
	{
		count = 1000;
		while(count--);
	}
}

/*----------------------------------
**函数名称：EXTI15_10_IRQHandler
**功能描述：按键中断服务函数
**参数说明：无
**作者：Andrew
**日期：2018.1.25
-----------------------------------*/
void EXTI15_10_IRQHandler(void)
{
	if(EXTI_GetITStatus(EXTI_Line15) != RESET) //如果按键按下，产生了中断
	{
		key_delay(200);//消抖
		if(GPIO_ReadInputDataBit(GPIOA,GPIO_Pin_15) == 0)//这里是pin_15,不要写成PinSouce15,不然无法执行
		{
			light_screen = !light_screen;//标志取反
		}
	}
	EXTI_ClearITPendingBit(EXTI_Line15);//清除中断
}



